#list sorting
print('unsorted list')
l1 = [0,4,2,1,3,7,9]
print(l1)
l1.sort()
print('sorted list')
print(l1)
#reversing the order of the list in decending order
print('reverse the list order')
l1.reverse()
print(l1)
#appending the list or adding something at the end of the list
print('45 is aadded or appended to the list')
l1.append(45)
print(l1) 
l1.insert(0, 544)  #adds 544 to the index no.= 0 of the list
print(l1)
l1.pop(0) #removes the number on the index 2
print(l1)
#same is the case with the remove command
l1.remove(45)
print(l1)